        2) 회사를 이용하여 구입한 재화 등의 대금, 기타 회사 이용에 관련하여 회원이 부담하는 채무를 정해진 기한까지 지급하지 않는 경우<br />
        3) 다른 이용자의 프로그램 또는 서비스 이용을 방해하거나 그 정보를 도용하는 등 전자상거래 질서를 위협하는 경우<br />
        4) 회사가 제공하는 프로그램 또는 서비스를 이용하여 법령 또는 이 약관이 금지하거나 공서양속에 반하는 행위를 하는 경우<br />
        5) 기타 회사의 영업을 불법적인 방법으로 방해하거나 지장을 초래하는 행위를 하는 경우<br />
      ③ 회사가 제2항 각 호의 사유를 이유로 회원의 자격을 제한 또는 정지 시킨 후에도 동일한 행위가 2회 이상 반복되거나 30일 이내에 그 사유가 시정되지 아니하는 경우 회사는 해당 회원의 회원자격을 상실시킬 수 있습니다.<br />
      ④ 회사가 회원자격을 상실시키는 경우에는 해당 회원의 등록 정보를 말소합니다. 이 경우 회원에게 이를 통지하고, 등록 정보 말소 전에 최소한 30일 이상의 기간을 정하여 소명할 기회를 부여합니다.<br />
      ⑤ 학습종료(휴회) 후 7일 내 학습 연장을 하지 않을 경우 180일 후 휴면 계정으로 전환되며, 휴면 기간(12개월) 이후 자동으로 탈회 처리 됩니다.<br /><br />

      <span class="bold">제8조(회원에 대한 통지)</span><br /><br />

      ① 회사가 회원에 대한 통지를 하는 경우, 회원이 회사와 미리 약정하여 지정한 전자우편 주소로 할 수 있습니다.<br />
      ② 회사는 불특정다수 회원에 대한 통지의 경우 1주일 이상 회사 게시판에 게시함으로서 개별 통지에 갈음할 수 있습니다. 다만, 회원 본인의 거래와 관련하여 중대한 영향을 미치는 사항에 대하여는 개별통지를 합니다.<br /><br />


      <span class="bold">제9조(구매신청 및 개인정보 제공 동의 등)</span><br /><br />

      ① 회사 이용자는 홈페이지상에서 다음 또는 이와 유사한 방법에 의하여 구매를 신청하며, 회사는 이용자가 구매신청을 함에 있어서 다음의 각 내용을 알기 쉽게 제공하여야 합니다.<br />
        1) 재화 등의 검색 및 선택<br />
        2) 받는 사람의 성명, 주소, 전화번호, 전자우편주소(또는 이동전화번호) 등의 입력<br />
        3) 약관내용, 청약철회권이 제한되는 서비스, 배송료․설치비 등의 비용부담과 관련한 내용에 대한 확인<br />
        4) 이 약관에 동의하고 위 3.호의 사항을 확인하거나 거부하는 표시 (예, 마우스 클릭)<br />
        5) 재화 등의 구매신청 및 이에 관한 확인 또는 회사의 확인에 대한 동의<br />
        6) 결제방법의 선택<br />
      ② 회사가 제3자에게 구매자 개인정보를 제공할 필요가 있는 경우 다음 각 호의 사항을 구매자에게 알리고 동의를 받아야 합니다.(동의를 받은 사항이 변경되는 경우에도 같습니다.)<br />
        1) 개인정보를 제공받는 자<br />
        2) 개인정보를 제공받는 자의 개인정보 이용목적<br />
        3) 제공하는 개인정보의 항목<br />
        4) 개인정보를 제공받는 자의 개인정보 보유 및 이용기간<br />
      ③ 회사가 제3자에게 구매자의 개인정보를 취급할 수 있도록 업무를 위탁하는 경우에는 다음 각 호의 내용을 구매자에게 알리고 동의를 받아야 합니다.(동의를 받은 사항이 변경되는 경우에도 같습니다.) 다만, 회사는 서비스 제공에 관한 계약이행을 위해 필요하고 구매자의 편의증진과 관련된 경우에는 「정보통신망 이용촉진 및 정보보호 등에 관한 법률」에서 정하고 있는 방법으로 개인정보 취급방침을 통해 알림으로써 고지절차와 동의절차를 거치지 않을 수 있습니다.<br />
        1) 개인정보 취급위탁을 받는 자<br />
        2) 개인정보 취급위탁을 하는 업무의 내용<br /><br />


      <span class="bold">제10조(계약의 성립)</span><br /><br />

      ① 이용자의 구매신청에 대한 회사의 승낙이 제12조 제1항에 따른 수신확인통지형태(구매확인문자 또는 이메일)로 구매자에게 도달한 시점에 계약이 성립한 것으로 봅니다.<br />
      ② 회사의 승낙에는 이용자의 구매신청에 대한 확인 및 판매가능 여부, 구매신청의 정정 및 취소 등에 관한 정보 등이 포함되어야 합니다. 특히, 미성년자와 계약을 체결하는 경우에는 법정대리인의 동의를 얻지 못하면 미성년자 본인 또는 법정대리인이 계약을 취소할 수 있다는 내용을 고지하여야 합니다.<br />
      ③ 회사는 이용자의 구매신청에 다음 각 호의 사유가 있는 경우 승낙하지 않을 수 있습니다.<br />
        1) 신청 내용에 허위, 기재누락, 오기가 있는 경우<br />
        2) 기타 구매신청을 승낙하는 것이 회사 기술상 현저히 지장이 있다고 판단하는 경우<br /><br />


      <span class="bold">제11조(지급방법)</span><br /><br />

      ① 구매자는 구매한 재화 등에 대한 대금을 다음 각 호의 방법으로 회사에게 지급할 수 있으며, 회사는 구매자의 지급방법에 대하여 재화 등의 대금 이외에 어떠한 명목의 수수료도 추가하여 징수할 수 없습니다.<br />
        1) 폰뱅킹, 인터넷뱅킹, 메일 뱅킹 등의 각종 계좌이체<br />
        2) 선불카드, 직불카드, 신용카드 등의 각종 카드결제<br />
        3) 온라인 무통장입금<br />
        4) 전자화폐에 의한 결제<br />
        5) 수령시 대금지급<br />
        6) 마일리지 등 회사가 지급한 포인트에 의한 결제<br />
        7) 회사와 계약을 맺었거나 회사가 인정한 상품권에 의한 결제<br />
        8) 기타 전자적 지급 방법에 의한 대금 지급 등<br />
      ② 회사는 부득이한 사정이 있는 경우 제1항 각 호의 대금지급방법 중 일부를 일시적으로 중단할 수 있고, 그러한 경우 즉시 그 사유를 구매자에게 고지하여야 합니다.<br /><br />


      <span class="bold">제12조(수신확인통지․구매신청 변경 및 취소)</span><br /><br />

      ① 회사는 이용자의 구매신청이 있는 경우 이용자에게 수신확인통지(구매확인문자 또는 이메일)를 합니다.<br />
      ② 수신확인통지를 받은 구매자는 의사표시의 불일치 등이 있는 경우에는 수신확인통지를 받은 후 즉시 구매신청 변경 및 취소를 요청할 수 있고 회사는 배송 전에 구매자의 요청이 있는 경우에는 지체 없이 그 요청에 따라 처리하여야 합니다. 다만 이미 대금을 지불한 경우에는 제15조의 청약철회 등에 관한 규정에 따릅니다.<br /><br />


      <span class="bold">제13조(재화 등의 공급)</span><br />

      ① 회사는 구매자와 재화 등의 공급시기에 관하여 별도의 약정이 없는 이상, 구매자가 청약을 한 날부터 7일 이내에 재화 등을 배송할 수 있도록 주문제작, 포장 등 기타 필요한 조치를 취합니다. 다만, 회사가 이미 재화 등의 대금의 전부 또는 일부를 받은 경우에는 대금의 전부 또는 일부를 받은 날부터 3영업일 이내에 조치를 취합니다.
      이때 회사는 구매자가 재화 등의 공급 절차 및 진행 사항을 확인할 수 있도록 적절한 조치를 합니다. 회사의 서비스가 소프트웨어로 구성되어 있는 경우 다운로드 형태로 배송을 갈음할 수 있습니다.<br />
      ② 회사는 구매자가 구매한 재화에 대해 배송수단, 수단별 배송비용 부담자, 수단별 배송기간 등을 명시합니다. 만약 회사가 약정 배송기간을 초과한 경우에는 그로 인한 이용자의 손해를 배상하여야 합니다. 다만 회사가 고의․과실이 없음을 입증한 경우에는 그러하지 아니합니다.<br />
      ③ 회사의 귀책사유로 인하여 재화 등의 파손, 배송오류 등의 문제가 발생할 경우 회사는 추가 배송비용 없이 재화 등을 재발송하거나 환불을 하여야 합니다. 단, 구매자가 환불을 받기 위하여는 반드시 14일 이내에 기존 배송상품의 문제를 회사에 문제제기해야 합니다.<br />
      ④ 제3항에 해당하지 않는 교환, 환불의 경우 반환에 필요한 비용은 구매자가 부담하여야 합니다.<br />
      ⑤ 환불시, 구매자는 체험용 무료상품을 포함한 상품 일체를 즉시 반납하여야 합니다. 환불은 구매자가 반납한 상품을 검수한 후에 진행됩니다.<br /><br />


      <span class="bold">제14조(환급)</span><br /><br />

      회사는 이용자가 구매를 신청한 재화 등을 품절 등의 사유로 인도 또는 제공을 할 수 없을 때에는 지체없이 그 사유를 이용자에게 통지하고 사전에 재화 등의 대금을 받은 경우에는 대금을 받은 날부터 3영업일 이내에 환급하거나 환급에 필요한 조치를 취합니다.<br /><br />


      <span class="bold">제15조(청약철회 등)</span><br /><br />

      ① 회사와 재화 등의 구매에 관한 계약을 체결한 구매자는 「전자상거래 등에서의 소비자보호에 관한 법률」 제13조 제2항에 따라 계약내용에 관한 서면을 받은 날 (그 서면을 받은 때보다 재화 등의 공급이 늦게 이루어진 경우에는 재화 등을 공급받거나 재화 등의 공급이 시작된 날을 말합니다)부터 14일 이내에는 청약의 철회를 할 수 있습니다. 다만, 청약철회에 관하여 「전자상거래 등에서의 소비자보호에 관한 법률」에 달리 정함이 있는 경우에는 동 법 규정에 따릅니다.<br />
      ② 구매자가 재화 등을 배송 받은 후 다음 각 호의 사유에 해당하는 경우에는 반품 및 교환을 할 수 없습니다.<br />
        1) 구매자에게 책임 있는 사유로 재화 및 보관용 박스/포장 등이 멸실, 훼손 또는 분실된 경우<br />
           (다만, 재화 등의 내용을 확인하기 위하여 포장 등을 훼손한 경우에는 청약철회를 할 수 있습니다)<br />
        2) 구매자의 사용 또는 일부 소비에 의하여 재화 등의 가치가 현저히 감소한 경우<br />
        3) 시간의 경과에 의하여 재판매가 곤란할 정도로 재화 등의 가치가 현저히 감소한 경우<br />
        4) 같은 성능을 지닌 재화 등으로 복제가 가능한 경우 그 원본인 재화 등의 포장을 훼손한 경우<br />
        5) 소프트웨어 복제가 가능하거나 또는 화면 갈무리(캡처) 등으로 회사 프로그램의 저작권 침해가 가능한 경우 그 프로그램의 유료서비스 이용을 위한 ID와 비밀번호로 최초 접속이 이루어진 경우<br />
      ③ 제2항 제2호 내지 제4호, 제5호의 경우에 회사가 사전에 청약철회 등이 제한되는 사실을 이용자가 쉽게 알 수 있는 곳에 명기하거나 시용상품을 제공하는 등의 조치를 하지 않았다면 구매자의 청약철회 등이 제한되지 않습니다.<br />
      ④ 구매자는 제1항 및 제2항의 규정에 불구하고 재화 등의 내용이 표시·광고 내용과 다르거나 계약내용과 다르게 이행된 때에는 당해 재화 등을 공급받은 날부터 3월 이내, 그 사실을 안 날 또는 알 수 있었던 날부터 30일 이내에 청약철회 등을 할 수 있습니다.<br />
      ⑤ 구매자는 회사가 정한 별칙의 사항에 따라 환불 또는 교환을 요청할 수 있습니다.<br />
      ⑥ 태블릿 및 교재 등 제공받은 구성품 및 재화 등을 이미 사용하거나 반납이 불가능할 경우 해당 구성품의 소비자 가격 및 운송비를 공제한 후 환불됩니다.(체험용 무료상품 제외)<br />

      <span class="bold">제16조(청약철회 등의 효과)</span><br /><br />

      ① 회사는 구매자로부터 재화 등을 반환받은 경우 3영업일 이내에 이미 지급받은 재화 등의 대금을 환급합니다. 이 경우 회사가 환급을 지연한 때에는 그 지연기간에 대하여 「전자상거래 등에서의 소비자보호에 관한 법률 시행령」제21조의2에서 정하는 지연이자율을 곱하여 산정한 지연이자를 지급해야합니다.<br />
      ② 회사는 위 대금을 환급함에 있어서 구매자가 신용카드 또는 전자화폐 등의 결제수단으로 재화 등의 대금을 지급한 때에는 지체 없이 당해 결제수단을 제공한 사업자로 하여금 재화 등의 대금의 청구를 정지 또는 취소하도록 요청합니다.<br />
      ③ 청약철회 등의 경우 공급받은 재화 등의 반환에 필요한 비용은 구매자가 부담합니다. 회사는 구매자에게 청약철회 등을 이유로 위약금 또는 손해배상을 청구하지 않습니다. 다만 재화 등의 내용이 표시·광고 내용과 다르거나 계약내용과 다르게 이행되어 청약철회 등을 하는 경우 재화 등의 반환에 필요한 비용은 회사가 부담합니다.<br />
